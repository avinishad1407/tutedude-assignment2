num = int(input("Enter a number: "))        # for user input and converting it to int type
if (num % 2 == 0):                          # % operation on the user input for determining the remainder. 
    print(f"{num} is an even number")       # If condition is true statement executed
else:
    print(f"{num} is an odd number")        # Else statement is executed if "IF" is not true.